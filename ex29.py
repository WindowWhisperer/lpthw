# What if

people = 20
cats =  30
dogs = 15

if people < cats:
    print("Too many cats! The world is doomed!")

if people > cats:
    print("Not many cats! Room for more!")

if people > dogs:
    print("Easy for walkies.")

if people < dogs:
    print("So many dogs!")

dogs += 5

if people >= dogs:
    print("People are greater than or equal to dogs.")

if people <= dogs:
    print("People are less than or equal to dogs.")

if people == dogs:
    print("People are dogs.")